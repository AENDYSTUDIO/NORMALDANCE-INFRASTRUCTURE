    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–¥–æ—Ä–æ–≤—å—è –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
    HEALTH_URL="$DEPLOY_URL/api/health"
    log_info "–ü—Ä–æ–≤–µ—Ä–∫–∞ endpoint: $HEALTH_URL"

    if curl -f -s "$HEALTH_URL" > /dev/null; then
        log_success "Health check –ø—Ä–æ–π–¥–µ–Ω"
    else
        log_warning "Health check –Ω–µ –ø—Ä–æ–π–¥–µ–Ω, –Ω–æ —Ä–∞–∑–≤–µ—Ä—Ç—ã–≤–∞–Ω–∏–µ –∑–∞–≤–µ—Ä—à–µ–Ω–æ"
    fi

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –æ—Å–Ω–æ–≤–Ω—ã—Ö —Å—Ç—Ä–∞–Ω–∏—Ü
    MAIN_PAGE="$DEPLOY_URL"
    if curl -f -s "$MAIN_PAGE" > /dev/null; then
        log_success "–ì–ª–∞–≤–Ω–∞—è —Å—Ç—Ä–∞–Ω–∏—Ü–∞ –¥–æ—Å—Ç—É–ø–Ω–∞"
    else
        log_warning "–ì–ª–∞–≤–Ω–∞—è —Å—Ç—Ä–∞–Ω–∏—Ü–∞ –Ω–µ–¥–æ—Å—Ç—É–ø–Ω–∞"
    fi
}

